<h3>1、PM2.5是指什么？对人体有何危害？</h3>
<p style = "line-height:2">
PM2.5，是对大气中直径小于等于2.5微米的可入肺颗粒物。肉眼是看不到的，它们可以在空气中漂浮数天，会对人体造成伤害，咳嗽、呼吸困难、甚至导致慢性支气管炎、心律失常。
</p>

<h3>2、AQI、API、IAQI分别是指什么？</h3>
<p style = "line-height:2">
AQI即空气质量指数,AQI数值越大、级别越高,说明空气污染状况越严重，对人体的健康危害也就越大。是根据二氧化硫、二氧化氮、 PM10、 PM2.5、臭氧、一氧化碳六项空气污染物计算出来的。
</p>
<p style = "line-height:2">
API表示城市的短期空气质量状况和变化趋势。包括：烟尘、总悬浮颗粒物、可吸入悬浮颗粒物（浮尘）、二氧化氮、二氧化硫、一氧化碳、臭氧、挥发性有机化合物等等。 
</p>
<p style = "line-height:2">
IAQI是各污染物根据不同的目标浓度限值折算成空气质量分指数。
</p>

<h3>3、检测PM2.5的IAQI的目的是什么？</h3>
<p>
为了给用户提供最实时的天气数据。我们将PM2.5实时的浓度值根据公式计算出PM2.5的IAQI。
</p>

<h3>4、雾天和雾霾天气的区别和危害？ </h3>
<p style = "line-height:2">
雾是由大量悬浮的小水滴形成的，雾天的湿度较大，而霾天是由大量悬浮的小固体颗粒（特别是PM2.5）形成的，能见度会随之变小，天空也看起来灰蒙蒙的。PM2.5正是形成灰霾天气的元凶。我们应警惕灰霾天给健康带来的危害。空气污染指数大于100时大家应减少外出锻炼，空气污染指数大于150时大家外出时应带上口罩，勤洗手。
</p>
